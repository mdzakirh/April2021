package learning.function;

public class PersonsInformation {
	
	public static String myName(String firstName, String lastName) {
		String concat = "My Name is: "+ firstName +" "+ lastName;
		return concat;
	}
	
	public static String emailIdAndPhoneNumber(String emailId, String phoneNumber) {
		String concat = "My email is: "+ emailId +" phone number: "+ phoneNumber;
		return concat;
	}

}
